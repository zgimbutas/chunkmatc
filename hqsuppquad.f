        subroutine hqsuppquad(norder,inode,xs,ws,npts) 
        implicit real *8 (a-h,o-z)
c
        dimension xs(*),ws(*)
c
        itype=2
c
c
        if( itype .eq. 0 ) then
        if( norder .eq. 1 ) call suppquad1(inode,xs,ws,npts)
        if( norder .eq. 2 ) call suppquad2(inode,xs,ws,npts)
        if( norder .eq. 3 ) call suppquad3(inode,xs,ws,npts)
        if( norder .eq. 4 ) call suppquad4(inode,xs,ws,npts)
        if( norder .eq. 5 ) call suppquad5(inode,xs,ws,npts)
        if( norder .eq. 6 ) call suppquad6(inode,xs,ws,npts)
        if( norder .eq. 8 ) call suppquad8(inode,xs,ws,npts)
        if( norder .eq. 10 ) call suppquad10(inode,xs,ws,npts)
        if( norder .eq. 12 ) call suppquad12(inode,xs,ws,npts)
        if( norder .eq. 16 ) call suppquad16(inode,xs,ws,npts)
        if( norder .eq. 20 ) call suppquad20(inode,xs,ws,npts)
        endif
c
c
        if( itype .eq. 1 ) then
        if( norder .eq. 1 ) call hsuppquad1(inode,xs,ws,npts)
        if( norder .eq. 2 ) call hsuppquad2(inode,xs,ws,npts)
        if( norder .eq. 3 ) call hsuppquad3(inode,xs,ws,npts)
        if( norder .eq. 4 ) call hsuppquad4(inode,xs,ws,npts)
        if( norder .eq. 5 ) call hsuppquad5(inode,xs,ws,npts)
        if( norder .eq. 6 ) call hsuppquad6(inode,xs,ws,npts)
        if( norder .eq. 8 ) call hsuppquad8(inode,xs,ws,npts)
        if( norder .eq. 10 ) call hsuppquad10(inode,xs,ws,npts)
        endif
c
c
        if( itype .eq. 2 ) then
        if( norder .eq. 1 ) call hqsuppquad1(inode,xs,ws,npts)
        if( norder .eq. 2 ) call hqsuppquad2(inode,xs,ws,npts)
        if( norder .eq. 3 ) call hqsuppquad3(inode,xs,ws,npts)
        if( norder .eq. 4 ) call hqsuppquad4(inode,xs,ws,npts)
        if( norder .eq. 5 ) call hqsuppquad5(inode,xs,ws,npts)
        if( norder .eq. 6 ) call hqsuppquad6(inode,xs,ws,npts)
        if( norder .eq. 8 ) call hqsuppquad8(inode,xs,ws,npts)
        if( norder .eq. 10 ) call hqsuppquad10(inode,xs,ws,npts)
        endif
c
c
        do i=1,npts        
        xs(i)=(xs(i)+1)/2
        ws(i)=ws(i)/2
        enddo
c
        return
        end
